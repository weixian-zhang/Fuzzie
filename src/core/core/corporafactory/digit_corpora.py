    def next_corpora(self):
        rand = random.randint(0,2)
        data = self.corporaSrategies[str(rand)]()
        return data
    
    def corpora_from_data_strategy(self):
        randIdx = random.randint(0, len(self.data) - 1)
        return self.data[randIdx]
        
    def float_corpora_strategy(self):
        min = str(self.intFloatMin) + ".1"
        max = str(self.intFloatMax) + ".9"
        return random.uniform(float(min), float(max))
    
    def int_corpora_strategy(self):
        return random.randint(self.intFloatMin, self.intFloatMax)
        
        
    def get_user_supplied_range(self, min, max):
        
        if (not isinstance(min, int) and not isinstance(min, float) or
            not isinstance(max, int) and not isinstance(max, float)):
            return self.next_corpora()
        
        return random.uniform(min, max)
            
        
    
         
        
    
    